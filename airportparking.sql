-- payment table
CREATE TABLE IF NOT EXISTS payment (
    payment_id INT(11) NOT NULL AUTO_INCREMENT,
    booking_id INT(11),
    payment_type ENUM('credit_debit_card', 'paypal') NOT NULL,
    amount DECIMAL(10,2) NOT NULL,
