-- Booking table
CREATE TABLE IF NOT EXISTS booking (
    booking_id INT(11) NOT NULL AUTO_INCREMENT,
    user_id INT(11),
    type_id INT(11),
    airport VARCHAR(255),
    car_park VARCHAR(255),
    from_date DATE,
    from_time TIME,
    to_date DATE,
    to_time TIME,
    status ENUM('pending', 'confirmed', 'canceled') NOT NULL DEFAULT 'pending',
    slot_id INT(11),
    PRIMARY KEY (booking_id),
    
    -- Foreign key references
    FOREIGN KEY (user_id) REFERENCES users(user_id) ON DELETE SET NULL,
    FOREIGN KEY (type_id) REFERENCES parking_type(type_id) ON DELETE SET NULL,
    FOREIGN KEY (slot_id) REFERENCES parking_space(slot_id) ON DELETE SET NULL
);

-- Cancellation table
CREATE TABLE IF NOT EXISTS cancellation (
    id INT(11) NOT NULL AUTO_INCREMENT,
    booking_id INT(11) NOT NULL,
    user_id INT(11) NOT NULL,
    refund_status ENUM('pending', 'rejected', 'approved') DEFAULT 'pending',
    cancelled_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    PRIMARY KEY (id),

    FOREIGN KEY (booking_id) REFERENCES booking(booking_id) ON DELETE CASCADE,
    FOREIGN KEY (user_id) REFERENCES users(user_id) ON DELETE CASCADE
);

-- Contact table
CREATE TABLE IF NOT EXISTS contact (
    Contact_id INT(11) NOT NULL AUTO_INCREMENT,
    Name VARCHAR(100) NOT NULL,
    Email VARCHAR(100) NOT NULL,
    Subject VARCHAR(150),
    Phone_number VARCHAR(20),
    Message TEXT NOT NULL,
    ContactDate TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    PRIMARY KEY (Contact_id)
);


